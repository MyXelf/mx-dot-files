#!/bin/bash

#
# Function: hcolors()
#
# Prints out the 'Handy Colors'
#
hcolors () {
  echo -e "${BLACK}\${BLACK}"
  echo -e "${BLUE}\${BLUE}         ${LIGHT_BLUE}\${LIGHT_BLUE}"
  echo -e "${GREEN}\${GREEN}       ${LIGHT_GREEN}\${LIGHT_GREEN}"
  echo -e "${CYAN}\${CYAN}         ${LIGHT_CYAN}\${LIGHT_CYAN}"
  echo -e "${RED}\${RED}           ${LIGHT_RED}\${LIGHT_RED}"
  echo -e "${PURPLE}\${PURPLE}     ${LIGHT_PURPLE}\${LIGHT_PURPLE}"
  echo -e "${BROWN}\${BROWN}            ${YELLOW}\${YELLOW}"
  echo -e "${GRAY}\${GRAY}         ${LIGHT_GRAY}\${LIGHT_GRAY}"
  echo -e "${WHITE}\${WHITE}\n"
  echo -e "${NCOLOR}\${NCOLOR} (No Color)"
}

